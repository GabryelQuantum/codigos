# Interpolação de strings
nome = "Alice"
idade = 30
frase = f"Meu nome é {nome} e eu tenho {idade} anos."
print(frase) # Meu nome é Alice e eu tenho 30 anos.

# Formatando números
numero = 2.718281828459045
frase = f"O número de Euler com 2 casas decimais é {numero:.2f}" # :.2f para 2 casas decimais
print(frase) # O número de Euler com 2 casas decimais é 2.72